package org.heepie.Calc;

public class Calc {
	public void add(int parm1, int parm2) {
		System.out.println(parm1 + parm2);
	}
	
	public void multi(int parm1, int parm2) {
		System.out.println(parm1 * parm2);
	}
	
	public void div(int parm1, int parm2) {
		System.out.println(parm1 / parm2);
	}
	
	public void minus(int parm1, int parm2) {
		System.out.println(parm1 - parm2);
	}
}
